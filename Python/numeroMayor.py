num = int(input("Ingrese el primer numero"))
num2 = int(input("Ingrese el segundo numero"))
if num > num2:
    print("El numero " , num , " es mayor que el numero " , num2 , "y la diferencia entre ambos es de " , num - num2)
if num < num2:
    print("El numero " , num2 , " es mayor que el numero " , num , "y la diferencia entre ambos es de " , num2 - num)
else:
    print("Los numeros son iguales")
